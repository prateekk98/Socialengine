class Core_Widget_MenuSocialSitesController extends Engine_Content_Widget_Abstract
{
  public function indexAction()
  {
    $this->view->navigation = $navigation = Engine_Api::_()
      ->getApi('menus', 'core')
      ->getNavigation('core_social_sites');
    if( count($this->view->navigation) == 0 ) {
      return $this->setNoRender();
    }
  }

  public function getCacheKey()
  {
    return 'core_social_sites';
  }
}
